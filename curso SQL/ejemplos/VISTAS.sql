DESC USER_ERRORS

SELECT NAME, TYPE, TEXT 
FROM USER_ERRORS
WHERE TYPE='VIEW'
AND NAME = 'VISTA1';

-- COMPROBAR EL ESTADO DE LA VISTA EN EL DICCIONARIO DE DATOS ORACLE

DESC USER_OBJECTS

SELECT STATUS 
FROM USER_OBJECTS
WHERE OBJECT_NAME='VISTA1';

-- CONSULTAR LA VISTA EN EL DICCIONARIO DE DATOS ORACLE

DESC USER_VIEWS

SELECT TEXT
FROM USER_VIEWS
WHERE VIEW_NAME='VISTA1';

